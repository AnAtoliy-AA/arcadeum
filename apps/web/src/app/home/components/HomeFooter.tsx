export function HomeFooter() {
  const { messages } = useLanguage();
  const homeCopy = messages.home ?? {};
  const { social, appName } = appConfig;

  const currentYear = new Date().getFullYear();

  return (
    <FooterSection>
      <FooterContent>
        <SocialContainer>
          <SocialTitle>{homeCopy.footerFollowUs ?? 'Follow Us'}</SocialTitle>
          <SocialLinks>
            {SOCIAL_ICONS.map((item) => {
              if (item.id === 'support') {
                return (
                  <SocialIcon
                    key={item.id}
                    as={Link}
                    href={routes.support}
                    aria-label={item.label}
                  >
                    {item.icon}
                  </SocialIcon>
                );
              }

              const href = social[item.id as keyof typeof social];
              if (!href) return null;

              return (
                <SocialIcon
                  key={item.id}
                  href={href}
                  target="_blank"
                  rel="noopener noreferrer"
                  aria-label={item.label}
                >
                  {item.icon}
                </SocialIcon>
              );
            })}
          </SocialLinks>
        </SocialContainer>

        <Copyright>
          {formatMessage(homeCopy.footerRights, {
            year: currentYear,
            appName,
          }) ?? `Â© ${currentYear} ${appName}. All rights reserved.`}
          <VersionText>v{appConfig.appVersion}</VersionText>
        </Copyright>
      </FooterContent>
    </FooterSection>
  );
}
